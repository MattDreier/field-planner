/**
 * Get a plant by its ID (searches built-in database, and optionally user plants)
 */
export function getPlantById(id: string, userPlants?: PlantData[]): PlantData | undefined {
  const builtIn = PLANT_DATABASE.find(plant => plant.id === id);
  if (builtIn) return builtIn;
  if (userPlants) return userPlants.find(p => p.id === id);
  return undefined;
}

/**
 * Get all plants in the database
 */
export function getAllPlants(): PlantData[] {
  return [...PLANT_DATABASE];
}

/**
 * Get all built-in plants merged with user plants
 */
export function getAllPlantsWithUserPlants(userPlants: PlantData[]): PlantData[] {
  return [...PLANT_DATABASE, ...userPlants];
}

/**
 * Get all unique species groups from both built-in and user plants
 */
export function getAllSpeciesGroups(userPlants: PlantData[] = []): string[] {
  const groups = new Set<string>();
  for (const plant of PLANT_DATABASE) {
    if (plant.speciesGroup) groups.add(plant.speciesGroup);
  }
  for (const plant of userPlants) {
    if (plant.speciesGroup) groups.add(plant.speciesGroup);
  }
  return [...groups].sort();
}

/**
 * Get plants by category
 */
export function getPlantsByCategory(
  category: 'annual' | 'perennial' | 'biennial' | 'tender-perennial' | 'hardy-annual' | 'cool-season-annual'
): PlantData[] {
  return PLANT_DATABASE.filter(plant => plant.category === category);
}

/**
 * Search plants by name, scientific name, special notes, colors, or kind
 */
export function searchPlants(query: string): PlantData[] {
  const lowerQuery = query.toLowerCase();

  return PLANT_DATABASE.filter(plant => {
    // Search in name
    if (plant.name.toLowerCase().includes(lowerQuery)) {
      return true;
    }

    // Search in scientific name
    if (plant.scientificName.toLowerCase().includes(lowerQuery)) {
      return true;
    }

    // Search in special notes
    if (plant.specialNotes.toLowerCase().includes(lowerQuery)) {
      return true;
    }

    // Search in colors (if present)
    if (plant.colors && plant.colors.some(color => color.toLowerCase().includes(lowerQuery))) {
      return true;
    }

    // Search in kind
    if (plant.kind.toLowerCase().includes(lowerQuery)) {
      return true;
    }

    return false;
  });
}

/**
 * Get plants by propagation method
 */
export function getPlantsByPropagationMethod(
  method: 'seed' | 'corm' | 'division' | 'transplant'
): PlantData[] {
  return PLANT_DATABASE.filter(plant => plant.propagationMethod === method);
}

/**
 * Get cut-and-come-again plants
 */
export function getCutAndComeAgainPlants(): PlantData[] {
  return PLANT_DATABASE.filter(plant => plant.cutAndComeAgain);
}

/**
 * Get plants suitable for beginners (fast germination, easy care)
 */
export function getBeginnerFriendlyPlants(): PlantData[] {
  return PLANT_DATABASE.filter(plant => {
    const hasFastGermination = plant.daysToGermination > 0 && plant.daysToGermination <= 7;
    const isEasy = plant.specialNotes.toLowerCase().includes('easy') ||
                   plant.specialNotes.toLowerCase().includes('beginner');
    return hasFastGermination || isEasy;
  });
}

/**
 * Get plants by minimum vase life (in days)
 */
export function getPlantsByVaseLife(minDays: number): PlantData[] {
  return PLANT_DATABASE.filter(plant => plant.vaseLifeDays !== undefined && plant.vaseLifeDays >= minDays);
}

/**
 * Get quick harvest plants (ready in 60 days or less from seed)
 */
export function getQuickHarvestPlants(): PlantData[] {
  return PLANT_DATABASE.filter(plant =>
    plant.daysToHarvest > 0 && plant.daysToHarvest <= 60
  );
}

/**
 * Get plants by color
 */
export function getPlantsByColor(color: string): PlantData[] {
  const lowerColor = color.toLowerCase();
  return PLANT_DATABASE.filter(plant =>
    plant.colors && plant.colors.some(c => c.toLowerCase().includes(lowerColor))
  );
}

/**
 * Get cool-season plants
 */
export function getCoolSeasonPlants(): PlantData[] {
  return PLANT_DATABASE.filter(plant =>
    plant.category === 'cool-season-annual' ||
    plant.category === 'hardy-annual' ||
    plant.specialNotes.toLowerCase().includes('cool season')
  );
}

/**
 * Get plants that don't require fertilizer or need minimal feeding
 */
export function getLowFertilizerPlants(): PlantData[] {
  return PLANT_DATABASE.filter(plant =>
    plant.fertilizer.toLowerCase().includes('do not fertilize') ||
    plant.fertilizer.toLowerCase().includes('minimal') ||
    plant.fertilizer.toLowerCase().includes('little needed') ||
    plant.fertilizer.toLowerCase().includes('low')
  );
}

